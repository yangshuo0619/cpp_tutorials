/*
STL:standard template library,标准模板库，涵盖了常用的数据结构 和 算法，并且具有 跨平台的特点。
基本理念是把数据 和 操作 分离。数据由容器类别来管理，操作由定制的算法来完成。迭代器在容器和算法之间充当粘合剂，使得任何算法都可以和任何容器进行交互
作用。 STL 含有 容器， 算法， 迭代器组件。
其中容器：
STL序列容器：vector,string,deque,list;
STL关联容器：set,multiset,map,multimap;
STL适配容器:stack,queue,priority_queue;

STL，标准模板库，是C++标准函数库的一个子集。
C++标准函数库的组件：
1.C标准函数库，eg.<stdio.h>,<cstdio>
2.语言支持
3.诊断，用于程序诊断和报错的功能，包含了 异常处理（exception handling),断言（assertions),错误代码(error number codes)。
4.通用工具。eg.动态内存管理工具，日期/时间处理工具。
5.字符串
6.国际化
7.容器，涵盖了许多数据结构，eg,链表,vector,queue,stack,string
8.算法，包含了大约70个通用算法，用于操作各种容器，同时也可操控内建数组。
9.迭代器，每个容器都有自己的迭代器，只有容器自己才知道如何访问自己的元素。其有点像指针，算法通过 迭代器 来定位和操作容器中的元素。
10.数值，提供了复数运算的支持。
11.输入/输出


 */