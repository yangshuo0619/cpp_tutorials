// 函数定义
//void swap(int& x, int& y)
void swap(int& x, int& y)
{
   int temp;
   temp = x; /* 保存地址 x 的值 */
   cout << temp;
   x = y;    /* 把 y 赋值给 x */
   cout << x ;
   y = temp; /* 把 x 赋值给 y  */
   cout << y;
  
   return;
}